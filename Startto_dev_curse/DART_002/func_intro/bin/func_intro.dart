// Funções são blocos de código que podem ser executados várias vezes.
// São usadas para executar uma tarefa específica, para assim evitar repetição de código.

// Como analagia, podemos pensar em uma função como uma receita de bolo.
// A receita é o código da função, e o bolo é o resultado da função.

void main() {
  // Chamando a função 'printSaudacao' para executar o código dentro dela.
  printSaudacao();
}

// Para criar uma função, usamos a palavra-chave 'void' seguida do nome da função.
// O nome da função deve ser descritivo, e deve começar com uma letra minúscula.

// A função abaixo imprime uma mensagem na tela.
void printSaudacao() {
  print('Saudações do Helder Henrique!');
  print('Seja bem-vindo(a)');
}